    public void cuentaprecios(double pretotal) {
   
        if(pretotal==-1.0){
           pdescuento.setText(String.valueOf(preciototal*0.2));
                preciototal =preciototal-(preciototal*0.2);
        }else{
            preciototal = pretotal;
            ptotal.setText(String.valueOf(preciototal));
            pIVA.setText(String.format("%.2f",(preciototal*0.21)));
            totalconIVA.setText(String.format("%.2f",(preciototal*1.21)));
            
        }
    }

